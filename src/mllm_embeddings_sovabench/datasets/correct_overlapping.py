def compute_iou(box1, box2):
    x1_1, y1_1, x2_1, y2_1 = box1
    x1_2, y1_2, x2_2, y2_2 = box2

    # Compute intersection coordinates
    inter_x1 = max(x1_1, x1_2)
    inter_y1 = max(y1_1, y1_2)
    inter_x2 = min(x2_1, x2_2)
    inter_y2 = min(y2_1, y2_2)

    # Compute width and height of intersection
    inter_w = max(0, inter_x2 - inter_x1)
    inter_h = max(0, inter_y2 - inter_y1)

    # Intersection area
    inter_area = inter_w * inter_h

    # Areas of individual boxes
    area1 = (x2_1 - x1_1) * (y2_1 - y1_1)
    area2 = (x2_2 - x1_2) * (y2_2 - y1_2)

    # Union area
    union_area = area1 + area2 - inter_area

    # IoU
    iou = inter_area / union_area if union_area != 0 else 0

    return iou

def compute_iou_timespan(span1, span2):
    start1, end1 = span1
    start2, end2 = span2

    # Compute intersection
    inter_start = max(start1, start2)
    inter_end = min(end1, end2)
    inter_length = max(0, inter_end - inter_start)

    # Compute union
    union_length = end1 - start1

    # IoU
    iou = inter_length / union_length if union_length != 0 else 0

    return iou

def filter_overlap(base_path, df, iou_bbox, ios_time, format_ol='all'):
    videofiles = dict()
    overlapping = list()
    pairs = dict()
    for _, row in df.iterrows():
        video_parts = row['video'].split('.')
        file = os.path.join(base_path, row['prefix'].replace('videos', 'annotations'), '.'.join(video_parts[:-3]) + '.jsonl')
        timespan = [int(video_parts[-2]), int(video_parts[-1])]

        with open(file, "r", encoding="utf-8") as f:
            video_anns = [json.loads(line) for line in f if line.strip()]
        
        idxs = [i for i, data in enumerate(video_anns) if data['name'] == video_parts[-3] and data['timespan'] == timespan]

        if row['video'] not in pairs:
            pairs[row['video']] = 0
        else:
            pairs[row['video']] += 1

        if file not in videofiles:
            videofiles[file] = [(idxs[pairs[row['video']]], row['index'])]
        else:
            videofiles[file].append((idxs[pairs[row['video']]], row['index']))
    
    for k, v in videofiles.items():
        annotations = list()
        nums = [n[0] + 1 for n in v]
        with open(k, 'r', encoding='utf-8') as f:
            for line_num, line in enumerate(f, 1):
                if line_num in nums:
                    annotations.append(json.loads(line))
        
        for i in range(len(annotations)):
            for j in range(len(annotations)):
                if i == j:
                    continue

                if compute_iou(annotations[i]['bbox'], annotations[j]['bbox']) > iou_bbox:
                    if annotations[i]['name'] == 'vehicle_starts':
                        span1 = [max(annotations[i]['timespan'][0] - 91, 0), annotations[i]['timespan'][1]]
                    elif annotations[i]['name'] == 'vehicle_stops':
                        span1 = [annotations[i]['timespan'][0], annotations[i]['timespan'][1] + 91]
                    else:
                        span1 = annotations[i]['timespan']
                    
                    if annotations[j]['name'] == 'vehicle_starts':
                        span2 = [max(annotations[j]['timespan'][0] - 91, 0), annotations[j]['timespan'][1]]
                    elif annotations[j]['name'] == 'vehicle_stops':
                        span2 = [annotations[j]['timespan'][0], annotations[j]['timespan'][1] + 91]
                    else:
                        span2 = annotations[j]['timespan']

                    if compute_iou_timespan(span1, span2) > ios_time:
                        antonym = MevaActivityMapping.antonym(MevaActivityType(annotations[i]['name']))
                        action1 = MevaActivityType(annotations[i]['name'])
                        action2 = MevaActivityType(annotations[j]['name'])

                        name1 = v[i][1]

                        if format_ol == 'all' and action2 not in [action1, antonym]:
                            if name1 not in overlapping:
                                overlapping.append(name1)
                        
                        if format_ol == 'opposite' and action2 == antonym:                         
                            if name1 not in overlapping:
                                overlapping.append(name1)
    
    return overlapping


def load_and_prepare(question_path, to_delete):
    df = pd.read_csv(question_path, sep='\t')
    selection = df[~df['video'].isin(to_delete)]
    selection.loc[selection['dim'] == 'vehicle_augmented', 'dim'] = 'vehicle'
    return selection


def build_interpair(selection, to_delete_overlapping, base_path):
    # Base filtering
    selection_interpair = selection[
        ~selection['subdim'].isin(['driving forward', 'reversing'])
    ]
    selection_interpair = selection_interpair[
        ~selection_interpair['video'].isin(to_delete_overlapping)
    ]

    # Remove high-overlap annotations
    overlapping = filter_overlap(base_path, selection_interpair, 0.5, 0.5)
    selection_interpair = selection_interpair[
        ~selection_interpair['index'].isin(overlapping)
    ]

    print(f"Inter-pair shape: {selection_interpair.shape}")
    print("Inter-pair distribution:")
    print(selection_interpair['subdim'].value_counts())

    # Debug: show remaining overlaps at 0.0 threshold
    overlapping = filter_overlap(base_path, selection_interpair, 0.0, 0.0)
    print(f"Overlapping kept (0.0/0.0): {len(overlapping)}")

    return selection_interpair


def build_intrapair(selection, base_path):
    overlapping = filter_overlap(base_path, selection, 0.0, 0.0, format_ol='opposite')
    selection_intrapair = selection[
        ~selection['index'].isin(overlapping)
    ]

    print(f"Intra-pair shape: {selection_intrapair.shape}")
    print("Intra-pair distribution:")
    print(selection_intrapair['subdim'].value_counts())

    return selection_intrapair


def main(
    question_path,
    output_interpair,
    output_intrapair,
    base_path
):
    # Load
    selection = load_and_prepare(question_path, to_delete)

    # Build interpair
    selection_interpair = build_interpair(selection, to_delete_overlapping, base_path)
    selection_interpair.to_csv(output_interpair, sep="\t", index=False)

    # Build intrapair
    selection_intrapair = build_intrapair(selection, base_path)
    selection_intrapair.to_csv(output_intrapair, sep="\t", index=False)


